     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validated = $request->validate([
            'form_name' => 'required',
            'form_slug' => 'required',
        ]);

        $model = new Form;
        $model->form_name = $request->form_name;
        $model->form_slug = $request->form_slug;
        $model->description = $request->description;
        $model->allowed_domains = $request->allowed_domains;
        $model->limit_one_response = $request->limit_one_response;
        $model->save();
        return redirect('dashboard')->with(['success' => 'Form Berhasil Dibuat']);
    }

